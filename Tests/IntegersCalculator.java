public class IntegersCalculator {

    /**
     * Returns a + b
     * 
     * @param a
     * @param b
     * @return a + b
     */

    
    public int add (int a, int b) {
        return (a + b);
    }
    
    /**
     * Returns a - b
     * 
     * @param a
     * @param b
     * @return a - b
     */


    public int substraction (int a, int b){
        return (a - b);
    }
    
    /**
     * Returns a * b
     * 
     * @param a
     * @param b
     * @return a * b
     */


    public int multiplication (int a, int b){
        return (a * b);
    }
    
    /**
     * Returns a / b
     * 
     * @param a
     * @param b
     * @return a / b
     */


    public int division (int a, int b){
        return (a / b);
    }
    
}
